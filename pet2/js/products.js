/* Допоміжне: побудова URL зображення.
   Локальні шляхи (assets/…) повертаємо як є; інакше — Unsplash із розміром. */
export function imgURL(id, w) {
  if (!id) return id;
  if (id.indexOf("assets/") === 0 || id.indexOf("/") !== -1 || /\.(jpg|jpeg|png|webp|avif|svg)$/i.test(id)) return id;
  return "https://images.unsplash.com/" + id + "?auto=format&fit=crop&w=" + (w || 700) + "&q=80";
}
export function catLabel(key) {
  const c = (CATEGORIES || []).find((x) => x.key === key);
  return c ? c.label : "";
}
/* Ціна для обраного розміру (S/M/L). Округлення до 10 грн. */
export function sizePrice(p, sizeKey) {
  if (!p) return 0;
  var s = (p.sizes || []).find(function (x) { return x.key === sizeKey; });
  var mult = s ? s.mult : 1;
  return Math.round((p.price * mult) / 10) * 10;
}
